package WrapperClasses;

public class String_Examples {

	
	public static void main(String[] args) {
		
		//String				
		String number = "78";	
		
		Byte.valueOf(number);	  //convert String to Byte		
		Short.valueOf(number);	  //convert String to Short		
		Integer.valueOf(number);  //convert String to Integer		
		Long.valueOf(number);     //convert String to Long		
		Float.valueOf(number);	  //convert String to Float		
		Double.valueOf(number);   //convert String to Double
				
		
	}
}
