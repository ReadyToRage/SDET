package WrapperClasses;

public class DataTypesExamples {

	public static void main(String[] args) {	

		//			byte: 8 bits, range from -128 to 127
		byte age = 25;

		//			short: 16 bits, range from -32,768 to 32,767
		short temperature = -10;

		//			int: 32 bits, range from -2^31 to 2^31 - 1
		int population = 1000000;

		//			long: 64 bits, range from -2^63 to 2^63 - 1
		long worldPopulation = 7000000000L;

		//			float: 32 bits, approximately 7 decimal digits of precision
		float price = 19.99f;

		//			double: 64 bits, approximately 15 decimal digits of precision
		double pi = 3.141592653589793;

		//			char: 16 bits, representing a Unicode character
		char grade = 'A';

		//			boolean: The size is not precisely defined; it represents true or false values.
		boolean isJavaFun = true;

		//reference data types String 
		String greeting = "Hello, World!";

		//reference data types Array 		
		int[] numbers = {1, 2, 3, 4, 5};

	}


}
